public class Penguin
{
    public static void main(String[] args)
    {
        printPenguin(2,3);
    }   
    
    // parameters used to define # penguins horizontally and # penguins vertically
    public static void printPenguin(int height, int width)
    {                           
        // for loop to print multiple penguins vertically
        for (int i = 1; i <= height; i++)
        {
            // series of print statements to print each line of the penguin
            // for loops to print multiple penguins horizontally
            for (int j = 1; j <= width; j++)
            {
                System.out.print("                                       88888888 ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("                                      8888888888");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("                (MMMMMMMMMMMMMM)      8888888888");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("              (MMMMMMMMMMMMMMMMMM)    8888888888");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("             (@        MM        @)    88888888 ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("            (@@     /\\    /\\     @@)      $     ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("            (&/     \\/    \\/     \\D)      |     ");
            }
            System.out.println();   
            for (int j = 1; j <= width; j++)
            {
                System.out.print("            (@       ______       @)      /     ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("             (@      \\\\  //      @)       |     ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("              @a      \\\\//      o@        \\     ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("            &######################D      |     ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("          (&&&####################DDD)    /     ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("         (&&&&(         ####      )DDD)   |     ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("        (&&&&&          ####       DDDDD) \\     ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("       (&&&&&(          ####       )DDDDD)|     ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("       &&&&&&&          |###       DDDDDDD/     ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("      (&&&&&&(          \\###       )DDDDDD)     ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("      &&&&&&&&           ####      DDDDDDDD     ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("      (&&&&&&&(                   )DDDDDDD)     ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("       &WWV &&&                   DDD VWWD      ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("        (WV  &&(                 )DD  VW)       ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("              &&                 DD             ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("               @@@@@@@@@@@@@@@@@@@              ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("                W@@@@@@@@@@@@@@@W               ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("                W      W W      W               ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.print("                WWW/\\WWW WWW/\\WWW               ");
            } 
            System.out.println();
            for (int j = 1; j <= width; j++)
            {
                System.out.println();
            } 
        } 
    } 
}
